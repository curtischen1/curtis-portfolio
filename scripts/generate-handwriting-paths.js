const opentype = require('opentype.js');
const fs = require('fs');
const path = require('path');

const FONT_PATH = path.join(__dirname, 'GloriaHallelujah-Regular.ttf');
const OUTPUT_PATH = path.join(__dirname, '..', 'src', 'components', 'handwriting-paths.ts');
const TEXT = "Hey, I'm Curtis";
const FONT_SIZE = 96;

// Approximate path length from SVG path commands
function approximatePathLength(commands) {
  let length = 0;
  let cx = 0, cy = 0;
  let startX = 0, startY = 0;

  for (const cmd of commands) {
    switch (cmd.type) {
      case 'M':
        cx = cmd.x; cy = cmd.y;
        startX = cx; startY = cy;
        break;
      case 'L': {
        const dx = cmd.x - cx, dy = cmd.y - cy;
        length += Math.sqrt(dx * dx + dy * dy);
        cx = cmd.x; cy = cmd.y;
        break;
      }
      case 'Q': {
        // Approximate quadratic bezier with line segments
        const steps = 10;
        for (let i = 1; i <= steps; i++) {
          const t = i / steps;
          const t1 = 1 - t;
          const nx = t1 * t1 * cx + 2 * t1 * t * cmd.x1 + t * t * cmd.x;
          const ny = t1 * t1 * cy + 2 * t1 * t * cmd.y1 + t * t * cmd.y;
          const px = ((i - 1) / steps);
          const pt = 1 - px;
          const prevX = pt * pt * cx + 2 * pt * px * cmd.x1 + px * px * cmd.x;
          const prevY = pt * pt * cy + 2 * pt * px * cmd.y1 + px * px * cmd.y;
          const dx = nx - prevX, dy = ny - prevY;
          length += Math.sqrt(dx * dx + dy * dy);
        }
        cx = cmd.x; cy = cmd.y;
        break;
      }
      case 'C': {
        // Approximate cubic bezier with line segments
        const steps = 20;
        let prevX = cx, prevY = cy;
        for (let i = 1; i <= steps; i++) {
          const t = i / steps;
          const t1 = 1 - t;
          const nx = t1*t1*t1*cx + 3*t1*t1*t*cmd.x1 + 3*t1*t*t*cmd.x2 + t*t*t*cmd.x;
          const ny = t1*t1*t1*cy + 3*t1*t1*t*cmd.y1 + 3*t1*t*t*cmd.y2 + t*t*t*cmd.y;
          const dx = nx - prevX, dy = ny - prevY;
          length += Math.sqrt(dx * dx + dy * dy);
          prevX = nx; prevY = ny;
        }
        cx = cmd.x; cy = cmd.y;
        break;
      }
      case 'Z': {
        const dx = startX - cx, dy = startY - cy;
        length += Math.sqrt(dx * dx + dy * dy);
        cx = startX; cy = startY;
        break;
      }
    }
  }
  return Math.round(length);
}

// Convert opentype path commands to SVG d string
function pathToSVG(commands) {
  return commands.map(cmd => {
    switch (cmd.type) {
      case 'M': return `M${r(cmd.x)} ${r(cmd.y)}`;
      case 'L': return `L${r(cmd.x)} ${r(cmd.y)}`;
      case 'Q': return `Q${r(cmd.x1)} ${r(cmd.y1)} ${r(cmd.x)} ${r(cmd.y)}`;
      case 'C': return `C${r(cmd.x1)} ${r(cmd.y1)} ${r(cmd.x2)} ${r(cmd.y2)} ${r(cmd.x)} ${r(cmd.y)}`;
      case 'Z': return 'Z';
      default: return '';
    }
  }).join(' ');
}

function r(n) {
  return Math.round(n * 100) / 100;
}

async function main() {
  const font = await opentype.load(FONT_PATH);

  const characters = [];
  let xOffset = 0;

  for (const char of TEXT) {
    const glyph = font.charToGlyph(char);
    const glyphPath = glyph.getPath(xOffset, FONT_SIZE, FONT_SIZE);
    const advance = (glyph.advanceWidth / font.unitsPerEm) * FONT_SIZE;

    if (char === ' ') {
      xOffset += advance;
      continue;
    }

    const d = pathToSVG(glyphPath.commands);
    const length = approximatePathLength(glyphPath.commands);

    characters.push({
      char,
      d,
      length: Math.max(length, 1),
    });

    xOffset += advance;
  }

  const totalWidth = Math.ceil(xOffset);
  const totalHeight = Math.ceil(FONT_SIZE * 1.4);
  const viewBox = `0 0 ${totalWidth} ${totalHeight}`;

  const output = `// Auto-generated by scripts/generate-handwriting-paths.js
// Do not edit manually - regenerate with: node scripts/generate-handwriting-paths.js

export interface HeroPath {
  char: string;
  d: string;
  length: number;
}

export const HERO_PATHS: HeroPath[] = ${JSON.stringify(characters, null, 2)};

export const VIEWBOX = '${viewBox}';
export const SVG_WIDTH = ${totalWidth};
export const SVG_HEIGHT = ${totalHeight};
`;

  fs.writeFileSync(OUTPUT_PATH, output, 'utf-8');
  console.log(`Generated ${characters.length} character paths`);
  console.log(`ViewBox: ${viewBox}`);
  console.log(`Output: ${OUTPUT_PATH}`);
}

main().catch(console.error);
